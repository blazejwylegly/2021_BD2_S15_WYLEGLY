package pl.polsl.s15.library.domain.orderItem;

import lombok.Getter;
import lombok.Setter;
import pl.polsl.s15.library.domain.order.Order;
import pl.polsl.s15.library.domain.orderable.Item;
import pl.polsl.s15.library.domain.strategy.AutoGeneratedId;

import javax.persistence.EmbeddedId;
import javax.persistence.Entity;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
@Getter
@Setter
@Entity(name = "order_items")
public class OrderItem {

    @EmbeddedId
    private AutoGeneratedId id;

    private Boolean isOrderItemReady;

    @ManyToOne
    @JoinColumn(name = "item_id", referencedColumnName = "id")
    private Item item;

    @ManyToOne
    @JoinColumn(name = "order_id", referencedColumnName = "id")
    private Order order;
}
