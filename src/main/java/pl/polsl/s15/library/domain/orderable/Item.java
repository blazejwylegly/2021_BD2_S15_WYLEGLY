package pl.polsl.s15.library.domain.orderable;

import lombok.Getter;
import lombok.Setter;
import pl.polsl.s15.library.domain.orderablePhoto.ItemPhoto;
import pl.polsl.s15.library.domain.strategy.AutoGeneratedId;

import javax.persistence.*;
import java.util.ArrayList;
import java.util.List;

@Getter
@Setter
@Entity(name = "items")
@MappedSuperclass
public abstract class Item {

    @EmbeddedId
    private AutoGeneratedId id;

    @Column(nullable = false)
    private String description;

    @Column(nullable = false)
    private String name;

    @Column(nullable = false)
    private String type;

    @ManyToMany
    @JoinTable(
            name = "photos_items",
            joinColumns = @JoinColumn(name = "item_id"),
            inverseJoinColumns = @JoinColumn(name = "photo_id")
    )
    private List<ItemPhoto> itemPhotos = new ArrayList<>();
}
