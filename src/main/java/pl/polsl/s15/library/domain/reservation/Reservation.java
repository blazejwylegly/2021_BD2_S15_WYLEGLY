package pl.polsl.s15.library.domain.reservation;

import pl.polsl.s15.library.domain.RentalItem;
import pl.polsl.s15.library.domain.strategy.AutoGeneratedId;

import javax.persistence.*;
import java.time.LocalDate;

@Entity(name = "reservations")
public class Reservation {

    @EmbeddedId
    private AutoGeneratedId id;

    @Column(nullable = false)
    private LocalDate startTime;

    @Column(nullable = false)
    private LocalDate endTime;

    @ManyToOne
    @JoinColumn(name = "rental_item_id", referencedColumnName = "id", nullable = false)
    private RentalItem rentalItem;
}
